#!/usr/bin/env node
/**
 * generate-codebase-map.mjs  —  WORKSPACE-LEVEL overview map
 *
 * Scans top-level sibling directories (other projects in the workspace) and
 * writes a high-level summary to CODEBASE_DEBUG_MAP.md.
 * This is the broader workspace map used by AI coding tools.
 *
 * For a deep, version-aware analysis of THIS project specifically, use:
 *   npm run arch:update  (scripts/generate-architecture-map.mjs)
 *   → writes docs/codebase-map.md and docs/codebase-map.json
 */

import fs from "node:fs";
import fsp from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const OUTPUT_FILE = path.join(ROOT, "CODEBASE_DEBUG_MAP.md");
const WATCH_DEBOUNCE_MS = 700;

const IGNORED_DIRS = new Set([
  ".git",
  ".next",
  "node_modules",
  "dist",
  "build",
  "coverage",
  ".turbo",
  ".cache",
  ".code-review-graph",
  ".venv",
  "venv",
]);

const PROJECT_KEY_DIRS = [
  "src",
  "app",
  "api",
  "prisma",
  "scripts",
  "public",
  "components",
  "lib",
  "hooks",
  "stores",
  "types",
  "docs",
];

function toPosix(p) {
  return p.split(path.sep).join("/");
}

function rel(p) {
  return toPosix(path.relative(ROOT, p)) || ".";
}

function shouldIgnoreDir(name) {
  return IGNORED_DIRS.has(name) || name.startsWith(".");
}

async function exists(p) {
  try {
    await fsp.access(p);
    return true;
  } catch {
    return false;
  }
}

async function readJsonSafe(filePath) {
  try {
    const raw = await fsp.readFile(filePath, "utf8");
    return JSON.parse(raw);
  } catch {
    return null;
  }
}

function detectFramework(pkg) {
  const deps = { ...(pkg.dependencies || {}), ...(pkg.devDependencies || {}) };
  if (deps.next) return "Next.js";
  if (deps.vite) return "Vite";
  if (deps.react) return "React";
  return "Node/Other";
}

function detectDataLayer(pkg) {
  const deps = { ...(pkg.dependencies || {}), ...(pkg.devDependencies || {}) };
  const layers = [];
  if (deps.prisma || deps["@prisma/client"]) layers.push("Prisma");
  if (deps["@supabase/supabase-js"]) layers.push("Supabase");
  if (deps.mongodb) layers.push("MongoDB");
  return layers.length ? layers.join(", ") : "None/Unknown";
}

function detectRuntime(pkg) {
  const scripts = pkg.scripts || {};
  const joined = JSON.stringify(scripts).toLowerCase();
  if (joined.includes("bun")) return "Bun + Node";
  return "Node";
}

function summarizeScripts(pkg) {
  const scripts = pkg.scripts || {};
  const keys = ["dev", "build", "start", "lint", "db:push", "db:migrate", "db:reset", "db:seed"];
  const present = keys.filter((k) => scripts[k]);
  return present.length ? present.join(", ") : "none";
}

async function listTopLevelDirectories() {
  const entries = await fsp.readdir(ROOT, { withFileTypes: true });
  return entries
    .filter((e) => e.isDirectory())
    .filter((e) => !shouldIgnoreDir(e.name))
    .map((e) => e.name)
    .sort((a, b) => a.localeCompare(b));
}

async function collectProjectInfo(dirName) {
  const dirPath = path.join(ROOT, dirName);
  const pkgPath = path.join(dirPath, "package.json");
  if (!(await exists(pkgPath))) return null;

  const pkg = await readJsonSafe(pkgPath);
  if (!pkg) return null;

  const keyDirs = [];
  for (const key of PROJECT_KEY_DIRS) {
    if (await exists(path.join(dirPath, key))) {
      keyDirs.push(key);
    }
  }

  return {
    dirName,
    packageName: pkg.name || "(unnamed)",
    version: pkg.version || "unknown",
    framework: detectFramework(pkg),
    dataLayer: detectDataLayer(pkg),
    runtime: detectRuntime(pkg),
    scriptsSummary: summarizeScripts(pkg),
    keyDirs,
  };
}

async function generateMap() {
  try {
    const topDirs = await listTopLevelDirectories();
    const projects = [];

    for (const d of topDirs) {
      try {
        const info = await collectProjectInfo(d);
        if (info) projects.push(info);
      } catch (err) {
        // Skip projects that fail to read
        console.error(`[map] Warning: Could not read project ${d}: ${err.message}`);
      }
    }

    const now = new Date().toISOString();

    const lines = [];
    lines.push("# Codebase Debug Map (Auto-Generated)");
    lines.push("");
    lines.push(`Generated: ${now}`);
    lines.push(`Workspace root: ${rel(ROOT)}`);
    lines.push("");
    lines.push("This file is generated by scripts/generate-codebase-map.mjs.");
    lines.push("Do not edit manually. Run npm run map:update to refresh.");
    lines.push("");

    lines.push("## Top-Level Tree");
    lines.push("");
    lines.push(`${path.basename(ROOT)}/`);
    for (const d of topDirs) {
      const marker = projects.find((p) => p.dirName === d) ? " [project]" : "";
      lines.push(`- ${d}/${marker}`);
    }
    lines.push("");

    lines.push("## Project Branches");
    lines.push("");
    if (!projects.length) {
      lines.push("No package.json-based projects found at top level.");
    } else {
      for (const p of projects) {
        lines.push(`### ${p.dirName}/`);
        lines.push(`- Package: ${p.packageName} (${p.version})`);
        lines.push(`- Stack: ${p.framework}`);
        lines.push(`- Runtime: ${p.runtime}`);
        lines.push(`- Data layer: ${p.dataLayer}`);
        lines.push(`- Common scripts: ${p.scriptsSummary}`);
        lines.push(`- Key dirs: ${p.keyDirs.length ? p.keyDirs.join(", ") : "none detected"}`);
        lines.push("");
      }
    }

    lines.push("## Shared Debug Entry Points");
    lines.push("");
    const rootEntrypoints = [
      "AGENTS.md",
      "package.json",
      "Caddyfile.txt",
      "start-dev.sh",
      "start-server.sh",
      "restart.sh",
      "prisma/schema.prisma",
      "src/",
      "scripts/",
    ];

    for (const ep of rootEntrypoints) {
      if (await exists(path.join(ROOT, ep))) {
        lines.push(`- ${toPosix(ep)}`);
      }
    }
    lines.push("");

    lines.push("## Tooling Contract");
    lines.push("");
    lines.push("- Coding tools should read this map first for structure awareness.");
    lines.push("- After adding/removing folders or major files, run npm run map:update.");
    lines.push("- For live updates while coding, run npm run map:watch in a terminal.");
    lines.push("");

    const content = `${lines.join("\n")}\n`;
    try {
      await fsp.writeFile(OUTPUT_FILE, content, "utf8");
    } catch (err) {
      console.error(`[map] Warning: Could not write to ${OUTPUT_FILE}: ${err.message}`);
      console.error("[map] Map not updated. Check disk space and permissions.");
    }
  } catch (err) {
    console.error(`[map] Map generation failed: ${err.message}`);
  }
}

function isRelevantChange(fileName) {
  if (!fileName) return false;

  const normalized = toPosix(fileName);

  if (normalized === "CODEBASE_DEBUG_MAP.md") return false;

  const parts = normalized.split("/");
  if (parts.some((part) => IGNORED_DIRS.has(part))) return false;

  return true;
}

async function runWatchMode() {
  await generateMap();
  console.log("[map] initial generation complete");

  let timer = null;
  let running = false;
  let queued = false;

  const schedule = () => {
    if (timer) clearTimeout(timer);
    timer = setTimeout(async () => {
      if (running) {
        queued = true;
        return;
      }
      running = true;
      try {
        await generateMap();
        console.log("[map] regenerated");
      } catch (err) {
        console.error("[map] regeneration failed", err);
      } finally {
        running = false;
        if (queued) {
          queued = false;
          schedule();
        }
      }
    }, WATCH_DEBOUNCE_MS);
  };

  const watcher = fs.watch(ROOT, { recursive: true }, (_event, fileName) => {
    if (!isRelevantChange(fileName)) return;
    schedule();
  });

  watcher.on("error", (err) => {
    console.error("[map] watcher error", err);
  });

  console.log("[map] watching for workspace changes");
}

async function main() {
  const watch = process.argv.includes("--watch");

  if (watch) {
    await runWatchMode();
    return;
  }

  await generateMap();
  console.log("[map] generated CODEBASE_DEBUG_MAP.md");
}

main().catch((err) => {
  console.error("[map] failed", err);
  process.exit(1);
});
